print(' ARITHMETIC OPERATORS '.center(50, '='))

print('\n' + ' ADDITION '.center(50, '~'))
print('1 + 1 =', 1 + 1)
print('1.0 + 1 =', 1.0 + 1)
print('1 + 1.0 =', 1 + 1.0)
print('1.0 + 1.0 =', 1.0 + 1.0)

print('\n' + ' SUBTRACTION '.center(50, '~'))
print('2 - 1 =', 2 + 1)
print('2.0 + 1 =', 2.0 + 1)
print('2 + 1.0 =', 2 + 1.0)
print('2.0 + 1.0 =', 2.0 + 1.0)

print('\n' + ' MULTIPLICATION '.center(50, '~'))
print('2 * 3 =', 2 + 3)
print('2.0 * 3 =', 2.0 + 3)
print('2 * 3.0 =', 2 + 3.0)
print('2.0 * 3.0 =', 2.0 + 3.0)

print('\n' + ' DIVISION '.center(50, '~'))
print('\n' + ' FLOAT DIVISION '.center(50, '-'))
print('3 / 2 =', 3 / 2)
print('3.0 / 2 =', 3.0 / 2)
print('3 / 2.0 =', 3 / 2.0)
print('3.0 / 2.0 =', 3.0 / 2.0)

print('\n' + ' INTEGER DIVISION '.center(50, '-'))
print('3 // 2 =', 3 // 2)
print('3.0 // 2 =', 3.0 // 2)
print('3 // 2.0 =', 3 // 2.0)
print('3.0 // 2.0 =', 3.0 // 2.0)

print('\n' + ' EXPONENTIATION '.center(50, '~'))
print('2 ** 4 =', 2 ** 4)
